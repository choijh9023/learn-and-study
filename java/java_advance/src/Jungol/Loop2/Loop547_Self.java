package Jungol.Loop2;

public class Loop547_Self {
    public static void main(String[] args) {
            // 다시 공부해볼 필요가 있다.
        for(int a=2;a<=6;a++){ // 행만 알려주는 포문
            for(int b = a; b<=a+4;b++){
                System.out.print(b + " ");


            }
            System.out.println();
        }
    }
}
