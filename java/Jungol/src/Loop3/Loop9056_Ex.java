package Loop3;

public class Loop9056_Ex {
    public static void main(String[] args) {
        for(int i =0;i<=2;i++){                 // (0,0)(0,1)(0,2)
            for(int j =0;j<=i;j++){             // (1,0)
                System.out.printf("*");         // (2,0)
                }                               // (3,0)
            System.out.println();
        }

    }
}
